		String a = "ゎ";
		int b = a.length();
		System.out.println(b);
		String c = "ゎ";
		int d = c.length();
		System.out.println(d);
		String e = "｝";
		int f = e.length();
		System.out.println(f);
		String g = "{";
		int h = g.length();
		System.out.println(h);
		String i = "}";
		int j = i.length();
		System.out.println(j);
		String k = "[";
		int l = k.length();
		System.out.println(l);
		String m = "]";
		int n = m.length();
		System.out.println(n);
		String o = "";
		int p = o.length();
		System.out.println(p);
		String q = "ゎ";
		int r = q.length();
		System.out.println(r);
		String s = "「";
		int t = s.length();
